public class Loops {

public static void main(String[] argv){

	for(int j = 0; j < 4; j++)
	{
		System.out.print ("HI!");
	} //for
	for (int j = 0; j < 4; j++)
	{
		System.out.println (5 - j);
	} //for
	for(int j = 1000; j <= 1005; j++)
	{
		System.out.println (5 - j);
	} //for
	for(int j = 10; j < 12; j++)
	{
		System.out.print ("Hello");
	} //for
	for (int j = 6; j > 2; j--)
	{
		System.out.print ("HI!");
	}
	for (int j = 0; j < 4; j += 2)
	{
		System.out.println (5 - j);
	}
	for (int j = 12; j <= 20; j--)
	{
		System.out.println (j);
	}
	for (int j = -5; j < 5; j++)
	{
		System.out.println (j);
	}
	for (int j = -3; j >= -21; j -= 3)
	{
		System.out.println (j);
	}
}
}