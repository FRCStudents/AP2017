public class Shape{
	private int numSides;
	private int height;
	private int width;
	
	protected int getNumSides(){
		return numSides;
	}

	protected void setNumSides(int j){
		numSides = j;
	}

	protected int getHeight(){
		return height;
	}

	protected void setHeight(int m){
		height = m;
	}

	protected int getWidth(){
		return width;
	}

	protected void setWidth(int l){
		width = l;
	}
	
}