public class Door{
	String color;
	boolean open;
	int width;
	int height;

	public void setColor(String x){
		color=x;
	}

	public String getColor(){
		return color;
	}

	public void setOpen(boolean x){
		open=x;
	}

	public boolean getOpen(){
		return open;
	}

	public void setWidth(int x){
		width=x;
	}

	public int getWidth(){
		return width;
	}

	public void setHeight(int x){
		height=x;
	}

	public int getHeight(){
		return height;
	}


}