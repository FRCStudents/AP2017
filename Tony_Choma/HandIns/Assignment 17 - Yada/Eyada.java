public class Eyada extends Yada{
        String eYadaString;
        int eYadaNumber;

        public EYada(){
                super();
                eYadaString = "E--Yada";
                eYadaNumber = 7;
        }

        public String toString(){
                return yadaString + eYadaString + yadaNumber + eYadaNumber;
        }
}
