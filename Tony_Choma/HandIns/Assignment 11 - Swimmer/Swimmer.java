public class Swimmer{
        String name;
        int weight;
        int height;
        int age;
        String country;
        int goldMedals;
        int silverMedals;
        int bronzeMedals;
        int bestTime;

        public String getName(){
                return name;
        }
        public void setName(String newName){
                name = newName;
        }

        public int getWeight(){
                return weight;
        }
        public void setWeight(int newWeight){
                weight = newWeight;
        }

        public int getHeight(){
                return height;
        }
        public void setHeight(int newHeight){
                height = newHeight;
        }

        public int getAge(){
                return age;
        }
        public void setAge(int newAge){
                age = newAge;
        }

        public String getCountry(){
                return country;
        }
        public void setCountry(String newCountry){
                country = newCountry;
        }

        public int getGoldMedals(){
                return goldMedals;
        }
        public void setGoldMedals(int newGoldMedals){
                goldMedals = newGoldMedals;
        }

        public int getSilverMedals(){
                return silverMedals;
        }
        public void setSilverMedals(int newSilverMedals){
                silverMedals = newSilverMedals;
        }

        public int getBronzeMedals(){
                return bronzeMedals;
        }
        public void setBronzeMedals(int newBronzeMedals){
                bronzeMedals = newBronzeMedals;
        }

        public int getBestTime(){
                return bestTime;
        }
        public void setBestTime(int newBestTime){
                bestTime = newBestTime;
        }
}
