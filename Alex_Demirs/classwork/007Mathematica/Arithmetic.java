public class Arithmetic {
	public static void main(String[] args) {

		int myNumber01 = 7 + 6;
		System.out.println(myNumber01);

		int myNumber02 = 7 - 6;
		System.out.println(myNumber02);

		int myNumber03 = 7 * 6;
		System.out.println(myNumber03);

		int myNumber04 = 7 / 6;
		System.out.println(myNumber04);

		int myNumber = 7 % 6;
		System.out.println(myNumber);


	}
}